'use strict'

let val1=prompt("Enter first Number");

let val2=prompt("Enter second Number");

let option = prompt("Enter operation add,sub,mul,div");

if(option === 'add'){
    console.log(val1+val2)
}
else if(option === 'add'){
    console.log(val1+val2)
}
else if(option === 'sub'){
    console.log(val1-val2)
}
else if(option === 'mul'){
    console.log(val1*val2)
}
else if(option === 'div'){
    console.log(val1/val2)
}